package com.acarballeira;

public class Preso {

    private String nome;
    private String apodo;
    private String codigo;
    private int anosSentencia;

    public Preso( String nome, String apodo, String codigo, int anosSentencia ) {
        this.nome = nome;
        this.apodo = apodo;
        this.codigo = codigo;
        this.anosSentencia = anosSentencia;
    }
}
